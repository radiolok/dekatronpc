"""
Tests for Dekatron — dekatron tube glow model.

Internal 30-bit Cathodes register drives 10-bit Out:
  Out[0]=Cathodes[0],  Out[1]=Cathodes[3],  Out[2]=Cathodes[6],
  Out[3]=Cathodes[9],  Out[4]=Cathodes[12], Out[5]=Cathodes[15],
  Out[6]=Cathodes[18], Out[7]=Cathodes[21], Out[8]=Cathodes[24],
  Out[9]=Cathodes[27]

Guide cathodes (right): 1,4,7,10,13,16,19,22,25,28
Guide cathodes (left):  2,5,8,11,14,17,20,23,26,29

Stepping requires a multi-cycle pulse sequence as generated by
DekatronPulseSender. The Dekatron uses two-phase pulsing:
  Pulses[0] and Pulses[1] fire in sequence to advance through
  Main → Guide → Guide → Main.
"""

import cocotb
from cocotb.clock import Clock
from cocotb.triggers import Timer, RisingEdge

import logging
log = logging.getLogger(__name__)

HS_CLK_PERIOD = 100  # ns


def _which_out(dut) -> int:
    """Return the index (0-9) of the active output, or -1 if none."""
    val = int(dut.Out.value)
    for i in range(10):
        if val & (1 << i):
            return i
    return -1


def _out_onehot(dut) -> int:
    """Return the Out value as integer."""
    return int(dut.Out.value)


@cocotb.test()
async def test_dekatron_reset(dut):
    """Dekatron: after reset, Out[0]=1 (glow at position 0)."""
    clock = Clock(dut.hsClk, HS_CLK_PERIOD, unit="ns")
    cocotb.start_soon(clock.start())

    dut.Rst_n.value = 0
    dut.Pulses.value = 0
    dut.In_n.value = 0x3FF
    for _ in range(5):
        await RisingEdge(dut.hsClk)
    dut.Rst_n.value = 1
    for _ in range(3):
        await RisingEdge(dut.hsClk)

    out_val = _out_onehot(dut)
    pos = _which_out(dut)
    assert pos == 0, f"After reset expected Out[0]=1, got Out=0b{out_val:010b} (position {pos})"
    assert bin(out_val).count("1") == 1, (
        f"Expected exactly one output bit set after reset, got 0b{out_val:010b}"
    )


