@cocotb.test()
async def test_dekatron_step_right(dut):
    """Dekatron: step right from position 0 using two-phase pulse sequence."""
    clock = Clock(dut.hsClk, HS_CLK_PERIOD, unit="ns")
    cocotb.start_soon(clock.start())

    dut.Rst_n.value = 0
    dut.Pulses.value = 0
    dut.In_n.value = 0x3FF
    for _ in range(5):
        await RisingEdge(dut.hsClk)
    dut.Rst_n.value = 1
    for _ in range(3):
        await RisingEdge(dut.hsClk)

    pos_before = _which_out(dut)
    assert pos_before == 0, f"After reset expected position 0, got {pos_before}"

    # Step right: forward stepping uses Pulses[0]=1 first, then Pulses[1]=1.
    dut.Pulses.value = 1  # Pulses[0]=1, Pulses[1]=0
    for _ in range(5):
        await RisingEdge(dut.hsClk)

    dut.Pulses.value = 2  # Pulses[1]=1, Pulses[0]=0
    for _ in range(5):
        await RisingEdge(dut.hsClk)

    dut.Pulses.value = 0
    for _ in range(10):
        await RisingEdge(dut.hsClk)

    pos_after = _which_out(dut)
    assert pos_after != pos_before, (
        f"Position should change after stepping right, was {pos_before}, still {pos_after}"
    )


