@cocotb.test()
async def test_dekatron_write_overrides_step(dut):
    """Dekatron: write operation takes priority over stepping."""
    clock = Clock(dut.hsClk, HS_CLK_PERIOD, unit="ns")
    cocotb.start_soon(clock.start())

    dut.Rst_n.value = 0
    dut.Pulses.value = 0
    dut.In_n.value = 0x3FF
    for _ in range(5):
        await RisingEdge(dut.hsClk)
    dut.Rst_n.value = 1
    for _ in range(3):
        await RisingEdge(dut.hsClk)

    # Write to position 3 first
    dut.In_n.value = (~(1 << 3)) & 0x3FF
    dut.Pulses.value = 1
    await RisingEdge(dut.hsClk)
    dut.Pulses.value = 0
    dut.In_n.value = 0x3FF
    await RisingEdge(dut.hsClk)

    pos = _which_out(dut)
    assert pos == 3, f"Expected position 3 after write, got {pos}"

    # Now write to position 7 while pulsing (write overrides step)
    dut.In_n.value = (~(1 << 7)) & 0x3FF
    dut.Pulses.value = 2  # Step right pulse active
    await RisingEdge(dut.hsClk)
    dut.Pulses.value = 0
    dut.In_n.value = 0x3FF
    await RisingEdge(dut.hsClk)

    pos = _which_out(dut)
    assert pos == 7, f"Write should override step, expected 7, got {pos}"


