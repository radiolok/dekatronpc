@cocotb.test()
async def test_dekatron_write_all_positions(dut):
    """Dekatron: write to every position 0-9 and verify."""
    clock = Clock(dut.hsClk, HS_CLK_PERIOD, unit="ns")
    cocotb.start_soon(clock.start())

    dut.Rst_n.value = 0
    dut.Pulses.value = 0
    dut.In_n.value = 0x3FF
    for _ in range(5):
        await RisingEdge(dut.hsClk)
    dut.Rst_n.value = 1
    for _ in range(3):
        await RisingEdge(dut.hsClk)

    for target_pos in range(10):
        dut.In_n.value = (~(1 << target_pos)) & 0x3FF
        dut.Pulses.value = 1
        await RisingEdge(dut.hsClk)
        dut.Pulses.value = 0
        dut.In_n.value = 0x3FF
        await RisingEdge(dut.hsClk)

        pos = _which_out(dut)
        assert pos == target_pos, (
            f"Write to position {target_pos}: expected Out[{target_pos}]=1, got Out[{pos}]=1"
        )


@cocotb.test()
async def test_dekatron_multiple_steps_right(dut):
    """Dekatron: step right multiple positions, verify position changes."""
    clock = Clock(dut.hsClk, HS_CLK_PERIOD, unit="ns")
    cocotb.start_soon(clock.start())

    dut.Rst_n.value = 0
    dut.Pulses.value = 0
    dut.In_n.value = 0x3FF
    for _ in range(5):
        await RisingEdge(dut.hsClk)
    dut.Rst_n.value = 1
    for _ in range(3):
        await RisingEdge(dut.hsClk)

    positions_seen = {_which_out(dut)}

    for step in range(5):
        # Two-phase right step: Pulses[0]=1 first, then Pulses[1]=1
        dut.Pulses.value = 1
        for _ in range(5):
            await RisingEdge(dut.hsClk)
        dut.Pulses.value = 2
        for _ in range(5):
            await RisingEdge(dut.hsClk)
        dut.Pulses.value = 0
        for _ in range(8):
            await RisingEdge(dut.hsClk)

        pos = _which_out(dut)
        positions_seen.add(pos)
        log.info(f"After step {step + 1}: position {pos}")

    assert len(positions_seen) > 1, (
        f"Stepping should change position, seen only: {positions_seen}"
    )


@cocotb.test()
async def test_dekatron_wraparound(dut):
    """Dekatron: stepping eventually wraps around the 10-position ring."""
    clock = Clock(dut.hsClk, HS_CLK_PERIOD, unit="ns")
    cocotb.start_soon(clock.start())

    dut.Rst_n.value = 0
    dut.Pulses.value = 0
    dut.In_n.value = 0x3FF
    for _ in range(5):
        await RisingEdge(dut.hsClk)
    dut.Rst_n.value = 1
    for _ in range(3):
        await RisingEdge(dut.hsClk)

    # Write to position 9
    dut.In_n.value = (~(1 << 9)) & 0x3FF
    dut.Pulses.value = 1
    await RisingEdge(dut.hsClk)
    dut.Pulses.value = 0
    dut.In_n.value = 0x3FF
    await RisingEdge(dut.hsClk)

    pos = _which_out(dut)
    assert pos == 9, f"After write to 9 expected position 9, got {pos}"

    # Step right (forward) from 9: Pulses[0]=1 first, then Pulses[1]=1
    dut.Pulses.value = 1
    for _ in range(5):
        await RisingEdge(dut.hsClk)
    dut.Pulses.value = 2
    for _ in range(5):
        await RisingEdge(dut.hsClk)
    dut.Pulses.value = 0
    for _ in range(10):
        await RisingEdge(dut.hsClk)

    pos = _which_out(dut)
    # After stepping right past position 9, expect wraparound to position 0
    assert pos == 0, f"After stepping right from 9 expected wrap to 0, got {pos}"


